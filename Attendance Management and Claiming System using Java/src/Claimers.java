    private void b2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b2ActionPerformed
        try{
            Class.forName("java.sql.DriverManager");
            Connection c = DriverManager.getConnection("jdbc:mysql://localhost"
                    + ":3306/attendance","root","root");
            int r = Integer.parseInt(rno.getText());
            int t = Integer.parseInt((String) p.getSelectedItem());
            String y = (String) a.getSelectedItem();
            Statement st= c.createStatement();
            String s = "Insert into claim values("+r+","+t+",'"+y+"');";
            String w = "Update details set Tclass = Tclass+1,Mclass=Mclass+1 where regno="+r+";";
            String e = "Update details set percent = (Aclass*100)/Tclass";
            st.executeUpdate(w);
            st.executeUpdate(e);
            int h = st.executeUpdate(s);
            if(h>0)
                {JOptionPane.showMessageDialog(this,"Record Saved Successfully");
                 rno.setText("");
                 p.setSelectedIndex(-1);
                 a.setSelectedIndex(-1);
                 p.setEditable(false);
                 a.setEditable(false);
                 p.setEnabled(false);
                 a.setEnabled(false);
                 b2.setEnabled(false);}
               else
                {JOptionPane.showMessageDialog(this,"Record not Saved");}
            }
            catch(Exception e)
        {JOptionPane.showMessageDialog(this, e.getMessage());}                      // TODO add your handling code here:
    }//GEN-LAST:event_b2ActionPerformed

