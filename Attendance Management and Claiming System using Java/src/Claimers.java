    private void distributeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_distributeActionPerformed
        try{
            Class.forName("java.sql.DriverManager");
            Connection c = DriverManager.getConnection("jdbc:mysql://localhost"
                + ":3306/attendance","root","root");
            int q =Integer.parseInt(rno.getText());
            Statement st= c.createStatement();
            String s = "Select * from details where regno="+q+";";
            ResultSet rs = st.executeQuery(s);
            int qua=0;
            while(rs.next())
            {qua++;}
            if(qua>0)
            {   JOptionPane.showMessageDialog(this,"Data Found. Enter the Claim Details.");
                p.setEditable(true);
                a.setEditable(true);
                p.setEnabled(true);
                a.setEnabled(true);
                b2.setEnabled(true);}
            else
            {JOptionPane.showMessageDialog(this,"Data not Found. Enter Again");
             rno.setText("");
             rno.requestFocus(); }
        }
        catch(Exception e)
        {JOptionPane.showMessageDialog(this, e.getMessage());}                   // TODO add your handling code here:
    }//GEN-LAST:event_distributeActionPerformed

